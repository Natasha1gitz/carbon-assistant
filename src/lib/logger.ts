import pino from "pino";

// Structured JSON logger for backend API actions
// Uses pino-pretty only in development for readable console logs
export const logger = pino({
  level: process.env.LOG_LEVEL || "info",
  transport:
    process.env.NODE_ENV !== "production"
      ? {
          target: "pino-pretty",
          options: {
            colorize: true,
          },
        }
      : undefined,
});
